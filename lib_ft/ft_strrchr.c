#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	int		i;
	char	ac;

	i = ft_strlen(s);
	ac = c;
	while (i >= 0)
	{
		if (s[i] == ac)
			return ((char *)&s[i]);
		i--;
	}
	return (0);
}
